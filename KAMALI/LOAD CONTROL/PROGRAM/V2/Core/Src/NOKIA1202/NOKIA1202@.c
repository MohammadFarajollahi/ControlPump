{
	char i,j;
	
	int d = r - ' ';
	d *= 5;

	
	for(i = 0; i< 5; i++)
	{
	if(toggle==0)j=font_data[d++];else	j=~font_data[d++];
				
		lcdSpi(LCD_DATA | j);
	}
	
	if(toggle==0)lcdSpi(LCD_DATA | 0x00);else lcdSpi(LCD_DATA | 0xff); // 1px gap after each letter
	
}



